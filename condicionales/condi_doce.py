# Si el día está soleado, podemos hacer un picnic.

esta_soleando = input("Esta soleando: ")

if esta_soleando == "si" or esta_soleando == "SI":
    print("Esta solendo,", esta_soleando , "entonces salimos hacer PICNIC")

elif esta_soleando == "no" or esta_soleando == "NO":
    print("Esta solendo,", esta_soleando , "entonces no salimos hacer PICNIC")

else:
    print("Debes ingresar la opción correcta.")